package de.hackerspacebremen.data.entities;

import com.googlecode.objectify.annotation.Id;

import de.hackerspacebremen.data.annotations.Entity;
import de.hackerspacebremen.data.annotations.FormatPart;

@com.googlecode.objectify.annotation.Entity
@Entity(name="GCMAuth")
public final class GCMAuth implements BasicEntity{

	@Id
	@FormatPart(key="GCMAuth1")
	private Long id;
	
	@FormatPart(key="GCMAuth2")
	private String token;

	public Long getId() {
		return id;
	}

	public void setId(Long id) {
		this.id = id;
	}

	public String getToken() {
		return token;
	}

	public void setToken(String token) {
		this.token = token;
	}
}
